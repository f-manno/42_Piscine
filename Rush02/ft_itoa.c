char	*ft_itoa(int nbr)
{
	int		i;
	char	c;
	char	*st;
	int		div;
	int		unit;

	i = 0;
	while (i != 2)
	{
		if (nbr)
		{
			div = nbr / 10;
			st[i] = div + '0';
			i++;
			unit = nbr % 10;
			st[i] = unit + '0';
			i++;
		}
	}
	st[i] = '\0';
	return (st);
}
